package org.vaadin.hackme.spring.views.news;

public class NewsModel {

	private int id;
	private String article;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getArticle() {
		return article;
	}

	public void setArticle(String article) {
		this.article = article;
	}

}
